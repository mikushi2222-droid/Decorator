// ─── Main screen ──────────────────────────────────────────────────────────────

class TrainingScreen extends StatefulWidget {
  const TrainingScreen({super.key});

  @override
  State<TrainingScreen> createState() => _TrainingScreenState();
}

class _TrainingScreenState extends State<TrainingScreen> {
  final _searchC = TextEditingController();
  Difficulty? _diffFilter;
  String _query = '';

  @override
  void dispose() {
    _searchC.dispose();
    super.dispose();
  }

  List<TrainingArticle> get _filtered {
    final q = _query.toLowerCase();
    return _articles.where((a) {
      final matchDiff = _diffFilter == null || a.difficulty == _diffFilter;
      final matchQ = q.isEmpty ||
          a.title.toLowerCase().contains(q) ||
          a.subtitle.toLowerCase().contains(q) ||
          a.tags.any((t) => t.contains(q));
      return matchDiff && matchQ;
    }).toList();
  }

  @override
  Widget build(BuildContext context) {
    final filtered = _filtered;
    return Column(children: [
      _buildHeader(),
      Expanded(
        child: filtered.isEmpty
            ? const Center(
                child: Text('Ничего не найдено', style: TextStyle(color: Colors.grey)))
            : ListView.separated(
                padding: const EdgeInsets.fromLTRB(16, 8, 16, 24),
                itemCount: filtered.length,
                separatorBuilder: (_, __) => const SizedBox(height: 10),
                itemBuilder: (_, i) => _ArticleCard(
                  article: filtered[i],
                  onTap: () => Navigator.push(
                    context,
                    MaterialPageRoute(
                        builder: (_) => _ArticleDetailScreen(article: filtered[i])),
                  ),
                ),
              ),
      ),
    ]);
  }

  Widget _buildHeader() {
    return Container(
      color: Colors.white,
      child: Column(children: [
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 12, 16, 8),
          child: TextField(
            controller: _searchC,
            decoration: InputDecoration(
              hintText: 'Поиск по технике...',
              prefixIcon: const Icon(Icons.search, size: 20),
              suffixIcon: _query.isNotEmpty
                  ? IconButton(
                      icon: const Icon(Icons.clear, size: 18),
                      onPressed: () =>
                          setState(() { _searchC.clear(); _query = ''; }),
                    )
                  : null,
            ),
            onChanged: (v) => setState(() => _query = v),
          ),
        ),
        SingleChildScrollView(
          scrollDirection: Axis.horizontal,
          padding: const EdgeInsets.fromLTRB(12, 0, 12, 10),
          child: Row(children: [
            _filterChip('Все', null),
            const SizedBox(width: 6),
            ...Difficulty.values.map((d) => Padding(
              padding: const EdgeInsets.only(right: 6),
              child: _filterChip(d.label, d),
            )),
          ]),
        ),
      ]),
    );
  }

  Widget _filterChip(String label, Difficulty? d) {
    final active = _diffFilter == d;
    final color = d?.color ?? const Color(0xFF9A7B5F);
    return GestureDetector(
      onTap: () => setState(() => _diffFilter = d),
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 150),
        padding: const EdgeInsets.symmetric(horizontal: 14, vertical: 7),
        decoration: BoxDecoration(
          color: active ? color : color.withOpacity(0.08),
          borderRadius: BorderRadius.circular(20),
          border: Border.all(color: active ? color : color.withOpacity(0.25)),
        ),
        child: Text(label,
            style: TextStyle(
                fontSize: 12,
                fontWeight: FontWeight.w600,
                color: active ? Colors.white : color)),
      ),
    );
  }
}

